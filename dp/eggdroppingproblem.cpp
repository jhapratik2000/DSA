int eggdropping(int eggs,int floor)
{
    if(eggs==1)
    return floor;
    if(floor==0||floor==1)
    return floor;
    int mn=INT_MAX;
    for(int k=1;k<=floor;k++)
    {
      //we are taking max for a particular floor(k) cuz we dont know if for a particular floor it will break or not hence we are considering both the cases


    // ***if it breaks***//
    // let us say it breaks at kth floor ,then for the floors above it will break,so we need to check for "k-1" floors and eggs would be now eggs-1

    //*if it doesnt't break***//
    //then we would need to check for higher floors(floor-k) ,hence we will pass func(eggs,floor-k)

        int temp=1+max(eggdropping(eggs-1,floor-1),eggdropping(eggs,floor-k));
        mn=min(mn,temp);//we need min attempts out of all the worst cases from all the floors



    }
    return mn;

    

}
