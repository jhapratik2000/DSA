bool bipartitedfs(int node,vector<int>adj[],int color[])
{
if(color[node]==-1)//when we make a dfs call then we mark the node of the color with 1.and we look for its adj nodes and color if they arent colored with color opp to the parent color,and call the dfs call for each adj node,if it is already colored then if isnt equal to the parent color then continue or return false
color[node]=1;
for(auto it:adj[node])
{
    if(color[it]==-1)
    {
        color[it]=1-color[node];
        if(!bipartitedfs(it,adj,color))
        return false;
    }
    else if(color[it]==color[node])
    return false;

}
return true;

}
bool checkbipartite(vector<int>adj[],int n)
{
    int color[n];
    memset(color,-1,sizeof(color));
    for(int i=0;i<n;i++)
    {
        if(color[i]==-1)
        if(!bipartitedfs(i,adj,color))
        return false;

    }
    return true;

}
